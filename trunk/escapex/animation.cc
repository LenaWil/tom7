bool anflying::think (unsigned int now) {

  /* are we done? */
  if (pleft <= 0) {
    return true;
  }
    
  /* should probably compute based on the
     number of frames we missed, too */
  int go = util::minimum(speed, pleft);

  {
    int dx, dy;
    dirchange(d, dx, dy);
    px += dx * go;
    py += dy * go;
  }

  pleft -= go;

  /* set timer */
  nexttick = now + wait + AN_SLOW;

  /* more */
  return false;
}

